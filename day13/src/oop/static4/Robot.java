package oop.static4;

public class Robot {
	public static long multiply(long a, long b) {
		return a * b;
	}
	public static double multiply(double a, double b) {
		return a * b;
	}
 	
		
	public static long square(long a) {
		return a * a;
	}
	public static double circleWidth(double radius) {
		return radius * radius * 3.14;
	}
	public static double triangleWidth(double bottom, double height) {
		return (bottom * height) / 2.0;
	}
}
